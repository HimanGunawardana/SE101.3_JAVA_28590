
package lab.pkg05.pkg22;

public class Employee {
    private String Name;
    private int Age;
    private int Salary;
    
    //setter method1
    public void setName(String N)
    {
        Name=N;
    }
    //getter method1
    public String getName()
    {
        return Name;
    }
    
    //setter method2
    public void setAge(int x)
    {
        Age=x;
    }
    //getter method2
    public int getAge()
    {
        return Age;
    }
    
    //setter method3
    public void setSalary(int S)
    {
        Salary=S;
    }
    //getter method3
    public int getSalary()
    {
        return Salary;
    }
    
    
}